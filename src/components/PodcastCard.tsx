"use client";

import React, { useState } from "react";
import Link from "next/link";
import Image from "next/image";
import { Card, CardContent, CardHeader } from "@/components/ui/card";
import { Badge } from "@/components/ui/badge";
import type { Podcast } from "@/types/database";
import { Mic2, X } from "lucide-react";
import { cn } from "@/lib/utils";

interface PodcastCardProps {
  podcast: Podcast & { episode_count?: number };
  onRemove?: (id: string) => void;
  hasNewEpisodes?: boolean;
}

export const PodcastCard = React.memo(function PodcastCard({ podcast, onRemove, hasNewEpisodes }: PodcastCardProps) {
  const [isRemoving, setIsRemoving] = useState(false);

  const handleRemove = async (e: React.MouseEvent) => {
    e.preventDefault();
    e.stopPropagation();

    if (isRemoving || !onRemove) return;

    setIsRemoving(true);
    try {
      const response = await fetch(`/api/podcasts/${podcast.id}`, {
        method: 'DELETE',
      });

      if (response.ok) {
        onRemove(podcast.id);
      } else {
        console.error('Failed to remove podcast');
      }
    } catch (err) {
      console.error('Error removing podcast:', err);
    } finally {
      setIsRemoving(false);
    }
  };
  // Handle image_url that might be an array or invalid
  let imageUrl: string | null = null;
  const rawImageUrl = podcast.image_url;

  if (rawImageUrl) {
    try {
      // If it's already an array, get the first element
      if (Array.isArray(rawImageUrl)) {
        imageUrl = rawImageUrl[0] || null;
      }
      // If it's a JSON array string, extract the first URL
      else if (typeof rawImageUrl === 'string' && rawImageUrl.startsWith('[')) {
        const parsed = JSON.parse(rawImageUrl);
        imageUrl = Array.isArray(parsed) ? parsed[0] : rawImageUrl;
      }
      // Otherwise, use it as-is
      else {
        imageUrl = rawImageUrl;
      }

      // Validate URL
      if (imageUrl) {
        new URL(imageUrl);
      }
    } catch {
      imageUrl = null;
    }
  }

  return (
    <Link href={`/podcast/${podcast.id}`} className="block h-full">
      <div className="group h-full bg-white dark:bg-[#1e202e] dark:border dark:border-white/5 rounded-2xl overflow-hidden transition-all duration-300 shadow-[0_8px_30px_rgb(0,0,0,0.04)] hover:shadow-[0_8px_30px_rgb(0,0,0,0.08)] dark:shadow-none dark:hover:shadow-none hover:scale-[1.02]">
        <div className="relative aspect-square w-full bg-secondary">
          {imageUrl ? (
            <Image
              src={imageUrl}
              alt={podcast.title}
              fill
              className="object-cover"
              sizes="(max-width: 768px) 100vw, (max-width: 1200px) 50vw, 33vw"
            />
          ) : (
            <div className="flex h-full w-full items-center justify-center bg-secondary">
              <Mic2 className="h-12 w-12 text-muted-foreground/40" />
            </div>
          )}

          {/* NEW Badge */}
          {hasNewEpisodes && (
            <div className="absolute top-3 left-3 bg-primary text-primary-foreground text-[10px] font-bold px-2 py-0.5 rounded-full shadow-sm z-10">
              NEW
            </div>
          )}

          {/* Remove Button - Glassmorphism */}
          {onRemove && (
            <button
              onClick={handleRemove}
              disabled={isRemoving}
              className={cn(
                'absolute top-3 right-3 p-2 rounded-full transition-all duration-200 z-20',
                'bg-white/30 backdrop-blur-md border border-white/20 text-white shadow-sm',
                'hover:bg-white/50 hover:scale-105',
                isRemoving && 'opacity-50 cursor-wait'
              )}
              aria-label="Remove from My Podcasts"
            >
              <X className="h-3.5 w-3.5 drop-shadow-sm" />
            </button>
          )}
        </div>

        <div className="p-5">
          <h3 className="font-bold text-base leading-tight tracking-tight text-foreground line-clamp-1 mb-1 group-hover:text-primary transition-colors">
            {podcast.title}
          </h3>

          {podcast.author && (
            <p className="text-xs text-slate-500 dark:text-slate-400 line-clamp-1 mb-4">
              {podcast.author}
            </p>
          )}

          <div className="flex items-center gap-2 flex-wrap">
            {podcast.episode_count !== undefined && (
              <span className="text-xs text-slate-400 dark:text-slate-500 font-medium">
                {podcast.episode_count} eps
              </span>
            )}

            {podcast.language && (
              <span className="inline-flex items-center justify-center px-2 py-0.5 rounded text-[10px] font-medium bg-secondary text-muted-foreground">
                {podcast.language.toUpperCase()}
              </span>
            )}
          </div>
        </div>
      </div>
    </Link>
  );
});
